-- =============================================================================
-- NOTE
-- =============================================================================
-- This script assumes the EMP table already exists.
--
-- Execute the database setup script before running these examples.
-- =============================================================================



-- =============================================================================
-- SECTION 1 : SYSDATE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT SYSDATE - Display Current System Date
PROMPT ============================================================

SELECT SYSDATE
FROM DUAL;



-- =============================================================================
-- SECTION 2 : MONTHS_BETWEEN()
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT MONTHS_BETWEEN() - Number of Months Between Two Dates
PROMPT ============================================================

SELECT MONTHS_BETWEEN(
       '14-NOV-2014',
       '14-FEB-2014')
FROM DUAL;



-- =============================================================================
-- SECTION 3 : ADD_MONTHS()
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT ADD_MONTHS() - Add 9 Months
PROMPT ============================================================

SELECT ADD_MONTHS(
       '14-FEB-2014',
       9)
FROM DUAL;



PROMPT
PROMPT ============================================================
PROMPT ADD_MONTHS() - Subtract 9 Months
PROMPT ============================================================

SELECT ADD_MONTHS(
       '14-FEB-2014',
       -9)
FROM DUAL;



-- =============================================================================
-- SECTION 4 : NEXT_DAY()
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT NEXT_DAY() - Next Saturday
PROMPT ============================================================

SELECT NEXT_DAY(
       '08-DEC-2012',
       'SATURDAY')
FROM DUAL;



-- =============================================================================
-- SECTION 5 : LAST_DAY()
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT LAST_DAY() - Last Day of the Month
PROMPT ============================================================

SELECT LAST_DAY('18-JUN-2022')
FROM DUAL;



-- =============================================================================
-- SECTION 6 : TO_CHAR() - Date Conversion
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT TO_CHAR() - Display Hire Date in DD/MM Format
PROMPT ============================================================

SELECT L_NAME,
       TO_CHAR(HIRE_DATE, 'DD/MM')
FROM EMP;



-- =============================================================================
-- SECTION 7 : TO_CHAR() - Number Conversion
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT TO_CHAR() - Display Salary in Currency Format
PROMPT ============================================================

SELECT L_NAME,
       TO_CHAR(SALARY, '$99,999.99')
FROM EMP;



-- =============================================================================
-- End of Script
