-- =============================================================================
-- NOTE
-- =============================================================================
-- This script creates a sample BANK table and demonstrates DML operations.
-- Execute the statements sequentially for proper results.
-- =============================================================================



-- =============================================================================
-- SECTION 1 : CREATE SAMPLE TABLE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 1 : Create BANK Table
PROMPT ============================================================

CREATE TABLE BANK
(
    BNAME VARCHAR2(50),
    BHQ   VARCHAR2(50),
    BID   NUMBER
);



-- =============================================================================
-- SECTION 2 : INSERT COMMAND
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 2 : Insert One Record into BANK Table
PROMPT ============================================================

INSERT INTO BANK
VALUES ('State Bank', 'Hyderabad', 1);



-- =============================================================================
-- Display Inserted Data
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Verify Inserted Record
PROMPT ============================================================

SELECT *
FROM BANK;



-- =============================================================================
-- Insert Additional Records for UPDATE and DELETE Examples
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Insert Additional Sample Records
PROMPT ============================================================

INSERT INTO BANK
VALUES ('Axis Bank', 'Mumbai', 2);

INSERT INTO BANK
VALUES ('Canara Bank', 'Bengaluru', 3);

COMMIT;



-- =============================================================================
-- Display Current Data
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Current Data Before UPDATE
PROMPT ============================================================

SELECT *
FROM BANK;



-- =============================================================================
-- SECTION 3 : UPDATE COMMAND
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 3 : Update Bank Name
PROMPT ============================================================

UPDATE BANK
SET BNAME = 'Bank of Baroda'
WHERE BNAME = 'Axis Bank';



-- =============================================================================
-- Verify Updated Data
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Verify Updated Record
PROMPT ============================================================

SELECT *
FROM BANK;



-- =============================================================================
-- SECTION 4 : DELETE SPECIFIC ROW
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 4 : Delete State Bank Record
PROMPT ============================================================

DELETE FROM BANK
WHERE BNAME = 'State Bank';



-- =============================================================================
-- Verify Deleted Record
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Verify Deleted Record
PROMPT ============================================================

SELECT *
FROM BANK;



-- =============================================================================
-- SECTION 5 : DELETE ALL ROWS
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 5 : Delete All Records
PROMPT ============================================================

DELETE FROM BANK;



-- =============================================================================
-- Verify Empty Table
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Verify Table After DELETE
PROMPT ============================================================

SELECT *
FROM BANK;



-- =============================================================================
-- Commit Transaction
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Commit All Changes
PROMPT ============================================================

COMMIT;



-- =============================================================================
-- Drop Sample Table
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Drop BANK Table
PROMPT ============================================================

DROP TABLE BANK;



-- =============================================================================
-- End of Script
-- =============================================================================
