-- =============================================================================
-- NOTE
-- =============================================================================
-- This script creates a sample DEMO1 table and demonstrates
-- COMMIT, SAVEPOINT, ROLLBACK TO SAVEPOINT, and ROLLBACK.
--
-- Execute the statements sequentially to understand transaction behavior.
-- =============================================================================



-- =============================================================================
-- SECTION 1 : CREATE SAMPLE TABLE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 1 : Create DEMO1 Table
PROMPT ============================================================

CREATE TABLE DEMO1
(
    ID   NUMBER,
    NAME VARCHAR2(30),
    AGE  NUMBER
);



-- =============================================================================
-- SECTION 2 : INSERT INITIAL RECORDS
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 2 : Insert Initial Records
PROMPT ============================================================

INSERT INTO DEMO1
VALUES (1, 'A', 22);

INSERT INTO DEMO1
VALUES (2, 'B', 23);

INSERT INTO DEMO1
VALUES (3, 'C', 23);



-- =============================================================================
-- Display Data Before COMMIT
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Records Before COMMIT
PROMPT ============================================================

SELECT *
FROM DEMO1;



-- =============================================================================
-- SECTION 3 : COMMIT
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 3 : Commit Transaction
PROMPT ============================================================

COMMIT;



-- =============================================================================
-- SECTION 4 : INSERT NEW RECORD
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 4 : Insert Record After COMMIT
PROMPT ============================================================

INSERT INTO DEMO1
VALUES (4, 'D', 24);



-- =============================================================================
-- Verify Current Data
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Current Records
PROMPT ============================================================

SELECT *
FROM DEMO1;



-- =============================================================================
-- SECTION 5 : CREATE SAVEPOINT
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 5 : Create SAVEPOINT X
PROMPT ============================================================

SAVEPOINT X;



-- =============================================================================
-- SECTION 6 : INSERT RECORD AFTER SAVEPOINT
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 6 : Insert Record After SAVEPOINT
PROMPT ============================================================

INSERT INTO DEMO1
VALUES (5, 'P', 25);



-- =============================================================================
-- Verify Current Data
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Records After SAVEPOINT
PROMPT ============================================================

SELECT *
FROM DEMO1;



-- =============================================================================
-- SECTION 7 : ROLLBACK TO SAVEPOINT
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 7 : Rollback To SAVEPOINT X
PROMPT ============================================================

ROLLBACK TO X;



-- =============================================================================
-- Verify Data After Rollback To SAVEPOINT
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Records After ROLLBACK TO SAVEPOINT
PROMPT ============================================================

SELECT *
FROM DEMO1;



-- =============================================================================
-- SECTION 8 : ROLLBACK ENTIRE TRANSACTION
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 8 : Rollback Transaction
PROMPT ============================================================

ROLLBACK;



-- =============================================================================
-- Verify Final Data
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Final Records After ROLLBACK
PROMPT ============================================================

SELECT *
FROM DEMO1;



-- =============================================================================
-- Cleanup
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Drop DEMO1 Table
PROMPT ============================================================

DROP TABLE DEMO1;



-- =============================================================================
-- End of Script
-- =============================================================================
