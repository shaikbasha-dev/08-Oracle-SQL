-- =============================================================================
-- NOTE
-- =============================================================================
-- This script creates and modifies a sample BANK table for learning DDL
-- commands. Execute the statements one by one in the given order.
-- =============================================================================



-- =============================================================================
-- SECTION 1 : CREATE TABLE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 1 : Create BANK Table
PROMPT ============================================================

CREATE TABLE BANK
(
    BID   INT,
    BNAME VARCHAR2(10),
    BHQ   VARCHAR2(12)
);



-- =============================================================================
-- SECTION 2 : ALTER TABLE - ADD COLUMN
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 2 : Add Phone Number Column
PROMPT ============================================================

ALTER TABLE BANK
ADD PH_NO NUMBER;



-- =============================================================================
-- SECTION 3 : ALTER TABLE - DROP COLUMN
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 3 : Drop Phone Number Column
PROMPT ============================================================

ALTER TABLE BANK
DROP COLUMN PH_NO;



-- =============================================================================
-- SECTION 4 : ALTER TABLE - RENAME COLUMN
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 4 : Rename Column BNAME to BANKNAME
PROMPT ============================================================

ALTER TABLE BANK
RENAME COLUMN BNAME
TO BANKNAME;



-- =============================================================================
-- SECTION 5 : ALTER TABLE - RENAME TABLE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 5 : Rename Table BANK to BANK_DETAILS
PROMPT ============================================================

ALTER TABLE BANK
RENAME TO BANK_DETAILS;



-- =============================================================================
-- SECTION 6 : ALTER TABLE - MODIFY DATATYPE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 6 : Modify BID Datatype
PROMPT ============================================================

ALTER TABLE BANK_DETAILS
MODIFY BID VARCHAR2(54);



-- =============================================================================
-- SECTION 7 : ALTER TABLE - MODIFY COLUMN SIZE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 7 : Increase BANKNAME Column Size
PROMPT ============================================================

ALTER TABLE BANK_DETAILS
MODIFY BANKNAME VARCHAR2(97);



-- =============================================================================
-- SECTION 8 : ALTER TABLE - ADD PRIMARY KEY CONSTRAINT
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 8 : Add Primary Key Constraint
PROMPT ============================================================

ALTER TABLE BANK_DETAILS
ADD CONSTRAINT B_PK
PRIMARY KEY (BID);



-- =============================================================================
-- SECTION 9 : ALTER TABLE - DROP CONSTRAINT
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 9 : Drop Primary Key Constraint
PROMPT ============================================================

ALTER TABLE BANK_DETAILS
DROP CONSTRAINT B_PK;



-- =============================================================================
-- SECTION 10 : TRUNCATE TABLE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 10 : Truncate BANK_DETAILS Table
PROMPT ============================================================

TRUNCATE TABLE BANK_DETAILS;



-- =============================================================================
-- SECTION 11 : DROP TABLE
-- =============================================================================

PROMPT
PROMPT ============================================================
PROMPT Step 11 : Drop BANK_DETAILS Table
PROMPT ============================================================

DROP TABLE BANK_DETAILS;



-- =============================================================================
-- End of Script
-- =============================================================================
